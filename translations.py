def get_text(lang_code: str, key: str, default_lang: str = 'en') -> str:
    """
    Ottiene il testo tradotto per una chiave specifica
    """
    if lang_code in TRANSLATIONS and key in TRANSLATIONS[lang_code]:
        return TRANSLATIONS[lang_code][key]
    elif default_lang in TRANSLATIONS and key in TRANSLATIONS[default_lang]:
        return TRANSLATIONS[default_lang][key]
    else:
        return f"Missing translation: {key}"

